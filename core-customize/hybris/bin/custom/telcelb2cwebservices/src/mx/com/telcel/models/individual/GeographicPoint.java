package mx.com.telcel.models.individual;

public class GeographicPoint
{

	private String x;
	private String y;
	private String z;

	/**
	 * @return the x
	 */
	public String getX()
	{
		return x;
	}

	/**
	 * @param x
	 *           the x to set
	 */
	public void setX(final String x)
	{
		this.x = x;
	}

	/**
	 * @return the y
	 */
	public String getY()
	{
		return y;
	}

	/**
	 * @param y
	 *           the y to set
	 */
	public void setY(final String y)
	{
		this.y = y;
	}

	/**
	 * @return the z
	 */
	public String getZ()
	{
		return z;
	}

	/**
	 * @param z
	 *           the z to set
	 */
	public void setZ(final String z)
	{
		this.z = z;
	}

}
